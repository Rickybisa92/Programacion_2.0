public class ejemploFor {
    public static void main(String[] args) {
        // for ( inicializacion; condicion de bucle; incrementos o decrementos )
         int i = 1;
         while (i<10) {
             System.out.println(i);
             i++;
         }

        for (int j = 1; j < 10; j++) {
            System.out.println(j);
        }
    }
}
